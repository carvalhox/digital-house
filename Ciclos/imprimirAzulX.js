// um valor numérico “x” e imprima “Azul” “x” vezes. Por exemplo

//  imprimirAzul(3)
// "AzulAzulAzul"


function imprimirAzul(x){
    for (var i=0; i< x; i++){
        console.log("Azul")
    }
}

